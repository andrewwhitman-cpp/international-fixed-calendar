/** IFC upsides — About page "Why it helps" section. */
export const AboutBenefitsBody = `\
The thirteen 28-day months remove the uneven rhythm of 30- and 31-day months: every ordinary month has the same length, \
so planning across months becomes more uniform. Business quarters can align cleanly with four-week cycles, and recurring \
events tied to “the same weekday” within a month repeat in a steady pattern. The intercalary days handle the extra one or \
two days a year without breaking that inner symmetry.

Because the IFC can track the same civil year and leap rules as the Gregorian calendar, seasons and anniversaries tied to \
the solar year remain coherent—you are not trading scientific accuracy for neatness, only reorganizing how the interior \
of the year is labeled.`

/** Barriers to change — About page "Why change is hard" section. */
export const AboutDifficultiesBody = `\
Changing a civil calendar touches billions of habits at once: contracts, software, education, holidays, and archives are \
all expressed in today’s month names and date arithmetic. A transition would require long overlap periods, clear rules for \
old records, and updated systems—costly and slow even when the idea is sound.

Many communities also rely on calendars that are not Gregorian at all (lunar calendars, liturgical cycles, and so on). \
Adopting the IFC for civil dates would not replace those; it would add another layer to coordinate, which demands respect \
and patience. Finally, any global shift is political: agreement across countries is hard, and there are good reasons to move \
carefully rather than rush.`
